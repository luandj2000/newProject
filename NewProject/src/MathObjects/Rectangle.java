package MathObjects;

public class Rectangle extends Shape{

	public void Area(int length, int width) {
		System.out.println("The rectangle area is " + (length*width) + "cm\u00B2");
		
	}
	public void Area(double length, double width) {
		System.out.println("The rectangle area is " + (length*width) + "cm\u00B2");
		
	
	}
}
